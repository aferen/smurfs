
public class Mantar extends Obje {
	
	private int sayi = 1;

	private int puan = 50;

	private int maksSure = 20;
	
	private int beklemeSure = 7;
	
	public Mantar() {
		super();
	}

	public int getSayi() {
		return sayi;
	}

	public void setSayi(int sayi) {
		this.sayi = sayi;
	}

	public int getPuan() {
		return puan;
	}

	public void setPuan(int puan) {
		this.puan = puan;
	}

	public int getMaksSure() {
		return maksSure;
	}

	public void setMaksSure(int maksSure) {
		this.maksSure = maksSure;
	}

	public int getBeklemeSure() {
		return beklemeSure;
	}

	public void setBeklemeSure(int beklemeSure) {
		this.beklemeSure = beklemeSure;
	}

	
	
	
}
